type trainingDevelopmentDetailPosting struct {
	opt     *ficologic.PostingHubCreateOpt
	header  *hcmmodel.TrainingDevelopmentDetail
	jt      *hcmmodel.TDCJournalType
	trxType string
}

func NewTrainingDevelopmentDetailPosting(opt ficologic.PostingHubCreateOpt) *PostingHub[*hcmmodel.TrainingDevelopmentDetail, ficomodel.JournalLine] {
	c := new(trainingDevelopmentDetailPosting)
	c.opt = &opt
	pvd := PostingProvider[*hcmmodel.TrainingDevelopmentDetail, ficomodel.JournalLine](c)
	return NewPostingHub(pvd, opt)
}

func (p *trainingDevelopmentDetailPosting) Header() (*hcmmodel.TrainingDevelopmentDetail, *ficomodel.PostingProfile, error) {
	j, err := datahub.GetByID(p.opt.Db, new(hcmmodel.TrainingDevelopmentDetail), p.opt.JournalID)
	if err != nil {
		return nil, nil, fmt.Errorf("missing: journal: %s: %s", j.ID, err.Error())
	}
	p.header = j
	jt, err := datahub.GetByID(p.opt.Db, new(hcmmodel.TDCJournalType), j.JournalTypeID)
	if err != nil {
		return nil, nil, fmt.Errorf("missing: journal type: %s: %s", j.JournalTypeID, err.Error())
	}
	p.jt = jt
	p.trxType = p.jt.TransactionType

	j.PostingProfileID = p.jt.PostingProfileID
	pp, err := datahub.GetByID(p.opt.Db, new(ficomodel.PostingProfile), j.PostingProfileID)
	if err != nil {
		return nil, nil, fmt.Errorf("missing: posting profile: %s", j.PostingProfileID)
	}

	return j, pp, nil
}

